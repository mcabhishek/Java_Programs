public class RelationOperators
{
    public static void main(String args[])
    {
        int a = 10;
        int b = 20;
        int c = a - b;
        int d = b / 10;

        System.out.println("is a!=b " + (a != b));
        System.out.println("is a<b " + (a < b));
        System.out.println("is a==b " + (a == b));
        System.out.println("is a<=b " + (a <= b));
        System.out.println("is a>b " + (a > b));
        System.out.println("is a<=c " + (a <= c));
        System.out.println("is a>=d " + (a >= d));
    }
}